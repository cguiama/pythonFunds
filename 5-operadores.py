num1 = int(input("Digite o primeiro número\n"))
num2 = int(input("Digite o segundo número\n"))

# Aritméticos
sum = num1 + num2
sub = num1 - num2
div = num1 / num2
mult = num1 * num2
mod = num1 % num2
exponente = num1 ** num2
print(sum)
print(sub)
print(div)
print(mult)
print(mod)
print(f"O modulo de {num1} por {num2} é igual a {mod}.")
print(f"O número {num1} elevado a {num2} é igual a {exponente}.")
